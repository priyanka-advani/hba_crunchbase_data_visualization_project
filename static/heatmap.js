function getPoints() {

  points = [];
  $.get('/heatmap.json', function (heatmap_data) {
    var la, lo, w, point;
    for (var key in heatmap_data) {
      la = heatmap_data[key]['lat'];
      lo = heatmap_data[key]['lng'];
      w = heatmap_data[key]['weight'];
      point = {location: new google.maps.LatLng(la, lo), weight: w};
      points.push(point);
    }
  });
  return points;
}


function initMap() {

  map = new google.maps.Map(document.getElementById('heatmap'), {
    center: {lat: 39, lng: -95},
    zoom: 4,
    styles: stylesArray
  });

  heatmap = new google.maps.visualization.HeatmapLayer({
    data: getPoints(),
    maxIntensity: 300,
    radius: 12,
    map: map
  });

  setGradient();
  setLegendGradient();
}


function setGradient() {
  gradient = [
    'rgba(0, 255, 255, 0)',
    'rgba(0, 255, 255, 1)',
    'rgba(0, 191, 255, 1)',
    'rgba(0, 127, 255, 1)',
    'rgba(0, 63, 255, 1)',
    'rgba(0, 0, 255, 1)',
    'rgba(0, 0, 223, 1)',
    'rgba(0, 0, 191, 1)',
    'rgba(0, 0, 159, 1)',
    'rgba(0, 0, 127, 1)',
    'rgba(63, 0, 91, 1)',
    'rgba(127, 0, 63, 1)',
    'rgba(191, 0, 31, 1)',
    'rgba(255, 0, 0, 1)'
  ]
  heatmap.set('gradient', gradient);
}


function setLegendGradient() {
    var gradientCss = '(left';
    for (var i = 0; i < gradient.length; ++i) {
        gradientCss += ', ' + gradient[i];
    }
    gradientCss += ')';
    
    $('#legendGradient').css('background', '-webkit-linear-gradient' + gradientCss);
    $('#legendGradient').css('background', '-moz-linear-gradient' + gradientCss);
    $('#legendGradient').css('background', '-o-linear-gradient' + gradientCss);
    $('#legendGradient').css('background', 'linear-gradient' + gradientCss);
}
